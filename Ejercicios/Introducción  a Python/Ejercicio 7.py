
#7-Escribe un programa que calcule el área de un triángulo a partir de la base y la altura dadas.

base = int(input("Ingrese la base del triángulo: "))
altura = int(input("Ingrese la altura del triángulo: "))

area_del_triángulo = (base * altura) / 2

print("El área del triángulo es: ", area_del_triángulo)



