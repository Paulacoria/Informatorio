#9-Escribe un programa que solicite al usuario dos números y luego imprima la suma, la resta,
#la multiplicación y la división de los dos números.

numero1 = int(input("Ingrese el primer número aquí: "))
numero2 = int(input("Ingrese el segundo número aquí: "))

suma = numero1 + numero2
resta = numero1 - numero2
multiplicacion = numero1 * numero2
division = numero1 / numero2

print("La suma es: ", suma)
print("La resta es: ", resta)
print("La multiplicación es: ", multiplicacion)
print("La división es: ", division)



