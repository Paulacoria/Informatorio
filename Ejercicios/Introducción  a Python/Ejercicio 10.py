# 10-Crea un programa que pida al usuario una cantidad en dólares y la convierta a euros.
# Supón que el tipo de cambio es de 0.84 euros por dólar.

cantidad_de_dolares = int(input("Ingrese la cantidad de dolares aquí: "))

cantidad_de_euros = cantidad_de_dolares * 0.84

print("La cantidad de euros que tendrá será de: ", cantidad_de_euros)

