# 3-Escribir un programa que pida al usuario dos números y muestre por pantalla cuál de ellos es mayor.

numero1 = float(input("Ingrese un número: "))
numero2 = float(input("Ingrese otro número: "))

if numero1 > numero2:
    print("El número", numero1, "es mayor a", numero2)
else:
    print("El número", numero2, "es mayor a", numero1)






